            <p>Since 1992 Edward & Ive has quickly established a reputation for itself as a leading luxury brand that has set 
                the standard for contemporary tailoring and menswear.
The first of the ‘New Establishment’ tailors on Savile Row, Edward & Ive is proud to have contributed to revitalising what has long been acknowledged 
as the world centre for tailoring with its bold, progressive take on the timeless and traditional.<br><br>
Edward & Ive opened with one small store on Savile Row, a London address that has an incredible history and tradition of tailoring and men’s 
style spanning almost 200 years. Savile Row is renowned the world over as the byword for the best British tailoring, to the extent that even 
the Japanese word for suit is ‘Saburo’, a derivation of the street’s name.
We recognise and respect the traditions of Savile Row, but do so with boldness and vibrancy and an eye for the individual.
Our philosophy is to produce classic clothing of unsurpassable quality, but to push the boundaries through design, colour and cut.<br><br>
Service and expertise is also integral to Edward & Ive. And so too is craftsmanship – we work closely with the best woollen mills and the 
most exacting artisans in Britain and Italy to design and produce our exclusive fabrics and handmade accessories.
Edward & Ive remains a quintessentially British luxury company but now, with its online service and worldwide stockists operating in tandem with 
its stand alone shops on Savile Row, it is one with a distinctly international outlook.
Updated classics, timeless yet contemporary design and an exacting attention to detail - Edward & Ive is proud to set the benchmark for Savile Row 
tailoring, modern menswear and handcrafted accessories.</p>
